interface IconProps {
  name: keyof typeof iconMap; // Tipo per i nomi delle icone
  size?: number; // Dimensione opzionale
  color?: string; // Colore opzionale
}

const Icon: React.FC<IconProps> = ({
  name,
  size = 24,
  color = "currentColor",
}) => {
  const SelectedIcon = iconMap[name]; // Seleziona l'icona in base al nome

  if (!SelectedIcon) {
    return null; // Restituisci null se l'icona non esiste
  }

  return <SelectedIcon size={size} color={color} />;
};

export default Icon;
