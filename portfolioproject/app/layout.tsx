import type { Metadata } from "next";
import localFont from "next/font/local";
import "./globals.css";
import dynamic from "next/dynamic";
import Header from "@/src/components/Header";

const Navbar = dynamic(() => import("@/src/components/Navbar"));
const Footer = dynamic(() => import("@/src/components/Footer"));

const geistSans = localFont({
  src: "./fonts/GeistVF.woff",
  variable: "--font-geist-sans",
  weight: "100 900",
});

const geistMono = localFont({
  src: "./fonts/GeistMonoVF.woff",
  variable: "--font-geist-mono",
  weight: "100 900",
});

// Definisci i metadata di base
const metadata: Metadata = {
  title: "MyPortfolio",
  description: "Generated by create next app",
  icons: {
    icon: "/svg/logo.svg",
  },
};

export default function RootLayout({
  children,
  pageMetadata, // Aggiungi pageMetadata come props
}: {
  children: React.ReactNode;
  pageMetadata?: Metadata; // Permetti metadata personalizzati per ogni pagina
}) {
  const combinedMetadata = pageMetadata
    ? { ...metadata, ...pageMetadata }
    : metadata;

  return (
    <>
      <html lang="en">
        <Header metadata={combinedMetadata} />{" "}
        {/* Passa i metadata al componente Header */}
        <body
          className={`${geistSans.variable} ${geistMono.variable} antialiased min-h-screen flex flex-col`}
        >
          <Navbar />
          <main className="flex-grow mt-20 mb-20 px-4">{children}</main>
          <Footer />
        </body>
      </html>
    </>
  );
}
